package com.reportable.ReportFileUtil.Util;

/**
 * 可疑主体里的信息
 * @author 施文
 *
 */
public class CCIF
{
	String	CTAR	= null ;	// 详细地址
	String	CCTL	= null ;	// 联系电话
	String	CEML	= null ;	// 电子邮件
	public String getCTAR()
	{
		return CTAR ;
	}
	public void setCTAR(String cTAR)
	{
		CTAR = cTAR ;
	}
	public String getCCTL()
	{
		return CCTL ;
	}
	public void setCCTL(String cCTL)
	{
		CCTL = cCTL ;
	}
	public String getCEML()
	{
		return CEML ;
	}
	public void setCEML(String cEML)
	{
		CEML = cEML ;
	}
	
	
}
